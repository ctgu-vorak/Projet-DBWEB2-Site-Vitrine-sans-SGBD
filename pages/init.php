<h2>Contenu de présentation</h2>
<p align="justify">
Bienvenue à vous.<br>
Notre objectif ici est de vous proposer diverses anomalies fournies par notre partenaire attitré,
à savoir la <a href="http://fondationscp.wikidot.com/" target="_blank">Fondation SCP</a>.
En effet, depuis la nuit des temps, diverses anomalies errent dans notre monde et cette entreprise
a la capacité de pouvoir les contenir afin d'éviter de détruire l'humanité toute entière.
Cependant, les confinements de cette dernière arrivent à saturation et s'est résolue à se débarrasser 
de certaines d'entre-elles.<br>
<b>VCLS Recup.</b> est juste une plateforme de communication entre la Fondation et l'humanité.<br>
La Fondation s'occupera de la vente et vous confiera l'anomalie à vos risques et périls.
Vous pouvez également remplir le formulaire si vous constatez que vous avez une anomalie en votre possession.
</p>